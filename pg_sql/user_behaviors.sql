-- 用户行为表：记录用户与商品的交互行为
CREATE TABLE IF NOT EXISTS user_behaviors (
    id BIGSERIAL PRIMARY KEY,
    user_id INTEGER NOT NULL,
    product_id BIGINT NOT NULL,
    behavior_type VARCHAR(20) NOT NULL,
    behavior_weight DOUBLE PRECISION,
    stay_duration INTEGER,
    search_keyword VARCHAR(100),
    created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP
);

-- 创建索引
CREATE INDEX IF NOT EXISTS idx_user_behaviors_user_id ON user_behaviors(user_id);
CREATE INDEX IF NOT EXISTS idx_user_behaviors_product_id ON user_behaviors(product_id);
CREATE INDEX IF NOT EXISTS idx_user_behaviors_behavior_type ON user_behaviors(behavior_type);

-- 添加注释
COMMENT ON TABLE user_behaviors IS '用户行为数据表-记录用户与商品的交互行为';
COMMENT ON COLUMN user_behaviors.id IS '行为ID';
COMMENT ON COLUMN user_behaviors.user_id IS '用户ID';
COMMENT ON COLUMN user_behaviors.product_id IS '商品ID';
COMMENT ON COLUMN user_behaviors.behavior_type IS '行为类型：VIEW-浏览, SEARCH-搜索, FAVORITE-收藏, CART-加入购物车, PURCHASE-购买';
COMMENT ON COLUMN user_behaviors.behavior_weight IS '行为权重：VIEW=1.0, SEARCH=2.0, FAVORITE=3.0, CART=4.0, PURCHASE=5.0';
COMMENT ON COLUMN user_behaviors.stay_duration IS '停留时长（秒），用于浏览行为';
COMMENT ON COLUMN user_behaviors.search_keyword IS '搜索关键词';
COMMENT ON COLUMN user_behaviors.created_at IS '行为发生时间';

